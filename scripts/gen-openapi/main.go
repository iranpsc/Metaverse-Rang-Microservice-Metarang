// Command gen-openapi generates openapi/openapi.yaml from openapi/routes.yaml.
package main

import (
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

type routesFile struct {
	Routes []routeDef `yaml:"routes"`
}

type routeDef struct {
	Path     string     `yaml:"path"`
	Methods  []string   `yaml:"methods"`
	Tag      string     `yaml:"tag"`
	Summary  string     `yaml:"summary"`
	Security string     `yaml:"security"`
	Params   []paramDef `yaml:"params,omitempty"`
}

type paramDef struct {
	Name        string      `yaml:"name"`
	In          string      `yaml:"in"`
	Type        string      `yaml:"type"`
	Format      string      `yaml:"format,omitempty"`
	Description string      `yaml:"description,omitempty"`
	Example     interface{} `yaml:"example,omitempty"`
	Required    *bool       `yaml:"required,omitempty"`
}

var pathParamPattern = regexp.MustCompile(`\{([^}]+)\}`)

type openAPISpec struct {
	OpenAPI    string                 `yaml:"openapi"`
	Info       map[string]interface{} `yaml:"info"`
	Servers    []map[string]string    `yaml:"servers"`
	Tags       []map[string]string    `yaml:"tags"`
	Paths      map[string]interface{} `yaml:"paths"`
	Components map[string]interface{} `yaml:"components"`
}

func main() {
	root := findRepoRoot()
	routesPath := filepath.Join(root, "openapi", "routes.yaml")
	outPath := filepath.Join(root, "openapi", "openapi.yaml")
	handlerDir := filepath.Join(root, "services", "grpc-gateway", "internal", "handler")

	data, err := os.ReadFile(routesPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "read routes: %v\n", err)
		os.Exit(1)
	}

	var routes routesFile
	if err := yaml.Unmarshal(data, &routes); err != nil {
		fmt.Fprintf(os.Stderr, "parse routes: %v\n", err)
		os.Exit(1)
	}

	discovered, err := discoverHandlerEndpoints(handlerDir)
	if err != nil {
		fmt.Fprintf(os.Stderr, "discover handler params: %v\n", err)
		os.Exit(1)
	}

	augmentedRoutes := augmentRoutesFromDiscovery(routes.Routes, discovered)
	spec := buildSpec(augmentedRoutes, discovered)
	out, err := yaml.Marshal(spec)
	if err != nil {
		fmt.Fprintf(os.Stderr, "marshal spec: %v\n", err)
		os.Exit(1)
	}

	header := "# Generated by scripts/gen-openapi — do not edit by hand.\n# Sources: openapi/routes.yaml + grpc-gateway handler discovery — regenerate with: make openapi\n\n"
	if err := os.WriteFile(outPath, append([]byte(header), out...), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "write spec: %v\n", err)
		os.Exit(1)
	}

	fmt.Printf("Generated %s (%d routes, %d discovered endpoints)\n", outPath, len(augmentedRoutes), len(discovered))
}

func findRepoRoot() string {
	if wd, err := os.Getwd(); err == nil {
		dir := wd
		for i := 0; i < 6; i++ {
			if _, err := os.Stat(filepath.Join(dir, "openapi", "routes.yaml")); err == nil {
				return dir
			}
			parent := filepath.Dir(dir)
			if parent == dir {
				break
			}
			dir = parent
		}
	}
	return "."
}

func buildSpec(routes []routeDef, discovered map[string]discoveredEndpoint) openAPISpec {
	tagSet := map[string]struct{}{}
	paths := map[string]interface{}{}

	for _, route := range routes {
		tagSet[route.Tag] = struct{}{}
		pathItem, ok := paths[route.Path].(map[string]interface{})
		if !ok {
			pathItem = map[string]interface{}{}
			paths[route.Path] = pathItem
		}

		for _, method := range route.Methods {
			op := map[string]interface{}{
				"tags":        []string{route.Tag},
				"summary":     route.Summary,
				"operationId": operationID(route.Path, method),
				"responses": map[string]interface{}{
					"200": map[string]interface{}{
						"description": "Successful response",
					},
					"401": map[string]interface{}{
						"description": "Unauthenticated",
						"content": map[string]interface{}{
							"application/json": map[string]interface{}{
								"schema": map[string]string{"$ref": "#/components/schemas/Error"},
							},
						},
					},
					"422": map[string]interface{}{
						"description": "Validation error",
						"content": map[string]interface{}{
							"application/json": map[string]interface{}{
								"schema": map[string]string{"$ref": "#/components/schemas/ValidationError"},
							},
						},
					},
				},
			}

			if params := buildParameters(route, method, discovered); len(params) > 0 {
				op["parameters"] = params
			}

			if sec := securityRequirement(route.Security); sec != nil {
				op["security"] = sec
			}

			if methodNeedsBody(method) {
				op["requestBody"] = map[string]interface{}{
					"content": map[string]interface{}{
						"application/json": map[string]interface{}{
							"schema": map[string]string{"type": "object"},
						},
						"multipart/form-data": map[string]interface{}{
							"schema": map[string]string{"type": "object"},
						},
					},
				}
			}

			pathItem[strings.ToLower(method)] = op
		}
	}

	tags := make([]map[string]string, 0, len(tagSet))
	for tag := range tagSet {
		tags = append(tags, map[string]string{"name": tag})
	}
	sort.Slice(tags, func(i, j int) bool {
		return tags[i]["name"] < tags[j]["name"]
	})

	return openAPISpec{
		OpenAPI: "3.0.3",
		Info: map[string]interface{}{
			"title":       "metarang API",
			"description": "REST API for metarang microservices. Laravel-compatible JSON responses served via Kong → grpc-gateway → gRPC services.",
			"version":     "1.0.0",
			"contact": map[string]string{
				"name": "metarang",
			},
		},
		Servers: []map[string]string{
			{"url": "http://localhost:8000", "description": "Kong API Gateway (local)"},
			{"url": "http://localhost:8080", "description": "grpc-gateway direct (dev)"},
		},
		Tags:  tags,
		Paths: paths,
		Components: map[string]interface{}{
			"securitySchemes": map[string]interface{}{
				"BearerAuth": map[string]interface{}{
					"type":         "http",
					"scheme":       "bearer",
					"bearerFormat": "Token",
					"description":  "Laravel Sanctum-style bearer token. Header: Authorization: Bearer {token}",
				},
			},
			"schemas": map[string]interface{}{
				"Error": map[string]interface{}{
					"type": "object",
					"properties": map[string]interface{}{
						"message": map[string]string{"type": "string"},
					},
				},
				"ValidationError": map[string]interface{}{
					"type": "object",
					"properties": map[string]interface{}{
						"message": map[string]string{"type": "string"},
						"errors": map[string]interface{}{
							"type": "object",
							"additionalProperties": map[string]interface{}{
								"type":  "array",
								"items": map[string]string{"type": "string"},
							},
						},
					},
				},
			},
		},
	}
}

func securityRequirement(security string) []map[string][]string {
	switch security {
	case "bearer", "verified_email":
		return []map[string][]string{{"BearerAuth": {}}}
	case "optional", "guest":
		return []map[string][]string{
			{},
			{"BearerAuth": {}},
		}
	default:
		return nil
	}
}

func methodNeedsBody(method string) bool {
	switch strings.ToLower(method) {
	case "post", "put", "patch":
		return true
	default:
		return false
	}
}

func operationID(path, method string) string {
	slug := strings.TrimPrefix(path, "/api/")
	slug = strings.ReplaceAll(slug, "/", "_")
	slug = strings.ReplaceAll(slug, "{", "")
	slug = strings.ReplaceAll(slug, "}", "")
	return fmt.Sprintf("%s_%s", strings.ToLower(method), slug)
}

func buildParameters(route routeDef, method string, discovered map[string]discoveredEndpoint) []map[string]interface{} {
	overrides := map[string]paramDef{}
	for _, p := range route.Params {
		overrides[p.Name] = p
	}

	var pathParamDefs []paramDef
	var queryParamDefs []paramDef
	if ep, ok := lookupDiscovered(discovered, method, route.Path); ok {
		pathParamDefs = ep.PathParams
		queryParamDefs = ep.QueryParams
	}

	pathParams := mergeDiscoveredPathParams(route.Path, pathParamDefs, overrides)
	queryParams := mergeParamDefs(nil, queryParamDefs)
	for _, p := range route.Params {
		if p.In == "query" {
			queryParams = mergeParamDefs(queryParams, []paramDef{p})
		}
	}

	params := make([]map[string]interface{}, 0, len(pathParams)+len(queryParams))
	for _, p := range pathParams {
		if override, ok := overrides[p.Name]; ok && (override.In == "" || override.In == "path") {
			params = append(params, openAPIParameter(mergeParamDefs([]paramDef{p}, []paramDef{override})[0]))
			continue
		}
		params = append(params, openAPIParameter(p))
	}
	for _, p := range queryParams {
		params = append(params, openAPIParameter(p))
	}

	return params
}

func openAPIParameter(p paramDef) map[string]interface{} {
	in := p.In
	if in == "" {
		in = "query"
	}
	paramType := p.Type
	if paramType == "" {
		if in == "path" {
			paramType = inferPathParamType(p.Name)
		} else {
			paramType = inferQueryParamType(p.Name)
		}
	}

	schema := map[string]interface{}{"type": paramType}
	if p.Format != "" {
		schema["format"] = p.Format
	}
	if p.Example != nil {
		schema["example"] = p.Example
	} else if in == "path" {
		_, format, example := inferParamSchema(p.Name)
		if format != "" {
			schema["format"] = format
		}
		if example != nil {
			schema["example"] = example
		}
	}
	if paramType == "array" {
		schema["items"] = map[string]string{"type": "string"}
	}

	required := in == "path"
	if p.Required != nil {
		required = *p.Required
	}

	description := p.Description
	if description == "" && in == "path" {
		description = pathParamDescription(p.Name)
	}
	if description == "" && in == "query" {
		description = queryParamDescription(p.Name)
	}

	return map[string]interface{}{
		"name":        p.Name,
		"in":          in,
		"required":    required,
		"description": description,
		"schema":      schema,
	}
}

func augmentRoutesFromDiscovery(routes []routeDef, discovered map[string]discoveredEndpoint) []routeDef {
	known := map[string]struct{}{}
	for _, route := range routes {
		for _, method := range route.Methods {
			known[endpointKey(method, route.Path)] = struct{}{}
		}
	}

	augmented := append([]routeDef{}, routes...)
	keys := make([]string, 0, len(discovered))
	for key := range discovered {
		keys = append(keys, key)
	}
	sort.Strings(keys)

	for _, key := range keys {
		if _, ok := known[key]; ok {
			continue
		}
		ep := discovered[key]
		augmented = append(augmented, routeDef{
			Path:     ep.Path,
			Methods:  []string{strings.ToLower(ep.Method)},
			Tag:      inferTagFromPath(ep.Path),
			Summary:  ep.Summary,
			Security: inferSecurityFromPath(ep.Path),
		})
	}

	return augmented
}

func inferTagFromPath(path string) string {
	trimmed := strings.TrimPrefix(path, "/api/")
	parts := strings.Split(trimmed, "/")
	if len(parts) == 0 || parts[0] == "" {
		return "API"
	}
	segment := parts[0]
	words := strings.Fields(strings.NewReplacer("-", " ", "_", " ").Replace(segment))
	for i, word := range words {
		if len(word) == 0 {
			continue
		}
		words[i] = strings.ToUpper(word[:1]) + word[1:]
	}
	return strings.Join(words, " ")
}

func inferSecurityFromPath(path string) string {
	publicPrefixes := []string{
		"/api/auth/callback",
		"/api/auth/validate",
		"/api/citizen/",
		"/api/order/callback",
		"/api/upload",
		"/api/levels",
		"/api/maps",
		"/api/tutorials",
		"/api/video-tutorials",
		"/api/calendar",
		"/api/features",
		"/api/store",
	}
	for _, prefix := range publicPrefixes {
		if strings.HasPrefix(path, prefix) {
			return "optional"
		}
	}
	if strings.HasPrefix(path, "/api/auth/register") || strings.HasPrefix(path, "/api/auth/redirect") {
		return "guest"
	}
	return "bearer"
}

